namespace _02.ComparePerformanceOfOperators
{
    using System;

    public class MultiplicationMethods
    {
        public static int MultiplicationInt(int first, int second)
        {
            return first * second;
        }

        public static long MultiplicationLong(long first, long second)
        {
            return first * second;
        }

        public static float MultiplicationFloat(float first, float second)
        {
            return first * second;
        }

        public static double MultiplicationDouble(double first, double second)
        {
            return first * second;
        }

        public static decimal MultiplicationDecimal(decimal first, decimal second)
        {
            return first * second;
        }
    }
}
